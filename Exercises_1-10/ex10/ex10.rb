# puts a tab at the beginning
tabby_cat = "\tI'm tabbed in."

# puts a newline in the middle
persian_cat = "I'm split\non a line."

# escape backslashes, so one backslash shows up
backslash_cat = "I'm \\ a \\ cat."

# creates a multiple line string
fat_cat = """
I'll do a list:
\t* Cat food
\t* Fishies
\t* Catnip\n\t* Grass
"""

puts tabby_cat
puts persian_cat
puts backslash_cat
puts fat_cat
